package com.mobiletheatertech.plot;

public class Solid
{
	float height;
	float width;
	float depth;
	
	public Solid( float height, float width, float depth )
	{
		this.height = height;
		this.width = width;
		this.depth = depth;
	}
	
	public float getHeight()
	{
		return height;
	}
	
	public float getWidth()
	{
		return width;
	}
	
	public float getDepth()
	{
		return depth;
	}
}
